#!/usr/bin/env python3
from __future__ import annotations

import argparse
import csv
import json
import sys
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[2]
if str(REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(REPO_ROOT))

from cajas.reports.dataset_quality_research import build_dataset_quality_research_artifacts, render_dataset_quality_bundle_markdown  # noqa: E402
from cajas.reports.dataset_quality_schema_contract import (  # noqa: E402
    compute_drift_summary,
    detect_drift_against_golden,
    extract_schema_shape,
    validate_bundle_contract,
)
from cajas.reports.runtime_io_summary import safe_json_write  # noqa: E402


def _build_smoke_input(input_csv: Path, labels_csv: Path, out_csv: Path) -> None:
    label_by_dt: dict[str, str] = {}
    if labels_csv.exists():
        with labels_csv.open("r", encoding="utf-8", newline="") as f:
            for row in csv.DictReader(f):
                key = (row.get("datetime") or "").strip()
                if key:
                    label_by_dt[key] = (row.get("future_direction_8") or "").strip()

    with input_csv.open("r", encoding="utf-8", newline="") as src, out_csv.open("w", encoding="utf-8", newline="") as dst:
        reader = csv.DictReader(src)
        fieldnames = list(reader.fieldnames or [])
        out_fields = ["instrument", *fieldnames, "future_direction_8"]
        writer = csv.DictWriter(dst, fieldnames=out_fields)
        writer.writeheader()
        for row in reader:
            dt = (row.get("Time (UTC)") or "").strip()
            out_row = {"instrument": "EURUSD", **row, "future_direction_8": label_by_dt.get(dt, "flat")}
            writer.writerow(out_row)


def main() -> int:
    p = argparse.ArgumentParser(description="Run lightweight dataset-quality smoke workflow.")
    p.add_argument("--input", default="cajas/data_examples/validation_fixtures/eurusd_tiny.csv")
    p.add_argument("--labels", default="cajas/data_examples/validation_fixtures/eurusd_tiny_labels.csv")
    p.add_argument("--out-root", required=True)
    p.add_argument("--row-limit", type=int, default=10000)
    p.add_argument("--chunk-size", type=int, default=1000)
    p.add_argument("--include-real-data", action="store_true")
    p.add_argument("--allow-large-data", action="store_true")
    args = p.parse_args()

    out_root = Path(args.out_root).expanduser().resolve()
    out_root.mkdir(parents=True, exist_ok=True)
    work = out_root / "_work"
    work.mkdir(parents=True, exist_ok=True)
    prepared_csv = work / "smoke_input.csv"
    _build_smoke_input(Path(args.input), Path(args.labels), prepared_csv)

    row_limit = None if (args.include_real_data and args.allow_large_data) else args.row_limit
    bundle = build_dataset_quality_research_artifacts(
        input_csv=prepared_csv,
        label_columns=["future_direction_8"],
        feature_columns=["Open", "High", "Low", "Close", "Volume"],
        datetime_col="Time (UTC)",
        instrument_col="instrument",
        chunk_size=args.chunk_size,
        row_limit=row_limit,
    )
    md = render_dataset_quality_bundle_markdown(bundle=bundle)

    dq = out_root / "dataset_quality"
    dq.mkdir(parents=True, exist_ok=True)
    safe_json_write(dq / "dataset_quality_report.json", bundle["dataset_quality_report"])
    (dq / "dataset_quality_report.md").write_text(md["dataset_quality_report_md"], encoding="utf-8")

    labels = out_root / "labels"
    labels.mkdir(parents=True, exist_ok=True)
    label_diag = {
        "schema_version": bundle["dataset_quality_report"]["schema_version"],
        "scope": "offline_research_only",
        "label_diagnostics": bundle["dataset_quality_report"]["label_diagnostics"],
    }
    safe_json_write(labels / "label_coverage_diagnostics.json", label_diag)
    (labels / "label_coverage_diagnostics.md").write_text(
        "\n".join(["# Label Coverage Diagnostics", "", *[f"- {x['label']}: top_ratio={x['top_ratio']}" for x in label_diag["label_diagnostics"]]]) + "\n",
        encoding="utf-8",
    )

    tm = out_root / "time"
    tm.mkdir(parents=True, exist_ok=True)
    time_diag = {
        "schema_version": bundle["dataset_quality_report"]["schema_version"],
        "scope": "offline_research_only",
        "time_coverage": bundle["dataset_quality_report"]["time_coverage"],
        "instrument_summary": bundle["dataset_quality_report"]["instrument_summary"],
    }
    safe_json_write(tm / "time_coverage_diagnostics.json", time_diag)
    (tm / "time_coverage_diagnostics.md").write_text("# Time Coverage Diagnostics\n\n- generated by smoke workflow\n", encoding="utf-8")

    features = out_root / "features"
    features.mkdir(parents=True, exist_ok=True)
    dry_run = {
        "schema_version": bundle["dataset_quality_report"]["schema_version"],
        "scope": "offline_research_only",
        "chunked_feature_dry_run": bundle["dataset_quality_report"]["chunked_feature_dry_run"],
    }
    safe_json_write(features / "chunked_feature_dry_run.json", dry_run)
    (features / "chunked_feature_dry_run.md").write_text("# Chunked Feature Dry Run\n\n- generated by smoke workflow\n", encoding="utf-8")
    safe_json_write(features / "feature_schema_manifest.json", bundle["feature_schema_manifest"])
    (features / "feature_schema_manifest.md").write_text(md["feature_schema_manifest_md"], encoding="utf-8")

    queue = out_root / "research_queue"
    queue.mkdir(parents=True, exist_ok=True)
    safe_json_write(queue / "offline_research_queue_summary.json", bundle["offline_research_queue_summary"])
    (queue / "offline_research_queue_summary.md").write_text(md["offline_research_queue_summary_md"], encoding="utf-8")

    # Validate schema contracts
    contract_issues = validate_bundle_contract(bundle)
    error_count = sum(1 for i in contract_issues if i.severity == "error")
    warning_count = sum(1 for i in contract_issues if i.severity == "warning")

    # Validate semantic constraints
    from cajas.reports.dataset_quality_schema_contract import validate_semantic_constraints

    semantic_issues = []
    for report_key in ["dataset_quality_report"]:
        if report_key in bundle:
            semantic_issues.extend(validate_semantic_constraints(bundle[report_key], report_key))

    semantic_error_count = sum(1 for i in semantic_issues if i.severity == "error")
    semantic_warning_count = sum(1 for i in semantic_issues if i.severity == "warning")

    # Detect drift against golden shapes
    golden_dir = Path("cajas/data_examples/golden/dataset_quality")
    drift_items = []
    files_checked = 0

    if golden_dir.exists():
        report_golden_map = {
            "dataset_quality_report": "dataset_quality_report_shape.json",
            "feature_schema_manifest": "feature_schema_manifest_shape.json",
            "offline_research_queue_summary": "offline_research_queue_summary_shape.json",
        }
        for report_key, golden_file in report_golden_map.items():
            golden_path = golden_dir / golden_file
            if golden_path.exists() and report_key in bundle:
                files_checked += 1
                golden_shape = json.loads(golden_path.read_text(encoding="utf-8"))
                current_shape = extract_schema_shape(bundle[report_key], max_depth=4)
                drift_items.extend(detect_drift_against_golden(current_shape, golden_shape, golden_file, set()))

    drift_summary = compute_drift_summary(drift_items, files_checked)

    contract_dir = out_root / "contract"
    contract_dir.mkdir(parents=True, exist_ok=True)
    contract_report = {
        "status": "pass" if (error_count == 0 and semantic_error_count == 0) else "fail",
        "error_count": error_count,
        "warning_count": warning_count,
        "semantic_error_count": semantic_error_count,
        "semantic_warning_count": semantic_warning_count,
        "issues": [{"severity": i.severity, "path": i.path, "message": i.message} for i in contract_issues],
        "semantic_issues": [
            {"severity": i.severity, "path": i.path, "message": i.message, "expected": i.expected, "actual": i.actual}
            for i in semantic_issues
        ],
        "drift_summary": {
            "breaking_count": drift_summary.breaking_count,
            "additive_count": drift_summary.additive_count,
            "type_change_count": drift_summary.type_change_count,
            "missing_required_count": drift_summary.missing_required_count,
            "files_checked": drift_summary.files_checked,
            "files_with_drift": drift_summary.files_with_drift,
        },
        "drift_items": [
            {"file": d.file, "path": d.path, "kind": d.kind, "expected": d.expected, "actual": d.actual} for d in drift_items
        ],
    }
    safe_json_write(contract_dir / "dataset_quality_contract_report.json", contract_report)

    contract_md_lines = [
        "# Dataset Quality Contract Validation",
        "",
        f"- status: `{contract_report['status']}`",
        f"- error_count: `{error_count}`",
        f"- warning_count: `{warning_count}`",
        f"- semantic_error_count: `{semantic_error_count}`",
        f"- semantic_warning_count: `{semantic_warning_count}`",
        "",
        "## Drift Summary",
        "",
        f"- files_checked: `{drift_summary.files_checked}`",
        f"- files_with_drift: `{drift_summary.files_with_drift}`",
        f"- breaking_count: `{drift_summary.breaking_count}`",
        f"- additive_count: `{drift_summary.additive_count}`",
        f"- type_change_count: `{drift_summary.type_change_count}`",
        f"- missing_required_count: `{drift_summary.missing_required_count}`",
        "",
    ]

    if drift_summary.breaking_count > 0:
        contract_md_lines.append("## Breaking Drift")
        contract_md_lines.append("")
        for d in drift_items:
            if d.kind in ("missing_required", "removed", "type_change"):
                contract_md_lines.append(f"- [{d.kind}] {d.file}::{d.path}: expected={d.expected}, actual={d.actual}")
        contract_md_lines.append("")

    if drift_summary.additive_count > 0:
        contract_md_lines.append("## Additive Drift")
        contract_md_lines.append("")
        for d in drift_items:
            if d.kind == "additive":
                contract_md_lines.append(f"- [additive] {d.file}::{d.path}")
        contract_md_lines.append("")

    contract_md_lines.append("## Reviewer Note")
    contract_md_lines.append("")
    if semantic_error_count > 0:
        contract_md_lines.append("**Action required**: Semantic validation errors detected. Review and fix invalid field values.")
    elif drift_summary.breaking_count > 0:
        contract_md_lines.append("**Action required**: Breaking drift detected. Review changes and update golden shapes if intentional.")
    elif drift_summary.additive_count > 0:
        contract_md_lines.append("**Info**: Additive drift detected. New fields added. Review and update golden shapes if desired.")
    else:
        contract_md_lines.append("**No action needed**: No drift detected.")
    contract_md_lines.append("")

    if semantic_issues:
        contract_md_lines.append("## Semantic Issues")
        contract_md_lines.append("")
        for issue in semantic_issues:
            contract_md_lines.append(f"- [{issue.severity}] {issue.path}: {issue.message} (expected={issue.expected}, actual={issue.actual})")
        contract_md_lines.append("")

    if contract_issues:
        contract_md_lines.append("## Contract Issues")
        contract_md_lines.append("")
        for issue in contract_issues:
            contract_md_lines.append(f"- [{issue.severity}] {issue.path}: {issue.message}")
        contract_md_lines.append("")
    else:
        contract_md_lines.append("No contract issues found.")
        contract_md_lines.append("")
    (contract_dir / "dataset_quality_contract_report.md").write_text("\n".join(contract_md_lines) + "\n", encoding="utf-8")

    # Generate trend snapshot
    import datetime

    trend_snapshot = {
        "generated_at": datetime.datetime.now(datetime.timezone.utc).isoformat(),
        "quality_score": bundle["dataset_quality_report"]["quality_score"]["score"],
        "quality_grade": bundle["dataset_quality_report"]["quality_score"]["grade"],
        "status": bundle["dataset_quality_report"]["status"],
        "contract_status": contract_report["status"],
        "error_count": error_count,
        "warning_count": warning_count,
        "semantic_error_count": semantic_error_count,
        "semantic_warning_count": semantic_warning_count,
        "drift_breaking_count": drift_summary.breaking_count,
        "drift_additive_count": drift_summary.additive_count,
        "files_checked": drift_summary.files_checked,
        "row_count": bundle["dataset_quality_report"]["row_count"],
        "column_count": bundle["dataset_quality_report"]["column_count"],
    }
    safe_json_write(contract_dir / "dataset_quality_trend_snapshot.json", trend_snapshot)

    trend_md_lines = [
        "# Dataset Quality Trend Snapshot",
        "",
        f"- generated_at: `{trend_snapshot['generated_at']}`",
        f"- quality_score: `{trend_snapshot['quality_score']}`",
        f"- quality_grade: `{trend_snapshot['quality_grade']}`",
        f"- status: `{trend_snapshot['status']}`",
        f"- contract_status: `{trend_snapshot['contract_status']}`",
        "",
        "## Validation Counts",
        "",
        f"- error_count: `{trend_snapshot['error_count']}`",
        f"- warning_count: `{trend_snapshot['warning_count']}`",
        f"- semantic_error_count: `{trend_snapshot['semantic_error_count']}`",
        f"- semantic_warning_count: `{trend_snapshot['semantic_warning_count']}`",
        "",
        "## Drift Counts",
        "",
        f"- drift_breaking_count: `{trend_snapshot['drift_breaking_count']}`",
        f"- drift_additive_count: `{trend_snapshot['drift_additive_count']}`",
        f"- files_checked: `{trend_snapshot['files_checked']}`",
        "",
        "## Dataset Metrics",
        "",
        f"- row_count: `{trend_snapshot['row_count']}`",
        f"- column_count: `{trend_snapshot['column_count']}`",
        "",
    ]
    (contract_dir / "dataset_quality_trend_snapshot.md").write_text("\n".join(trend_md_lines) + "\n", encoding="utf-8")

    if error_count > 0 or semantic_error_count > 0:
        print(
            json.dumps(
                {
                    "status": "fail",
                    "error_count": error_count,
                    "semantic_error_count": semantic_error_count,
                    "out_root": str(out_root),
                },
                ensure_ascii=True,
            ),
            file=sys.stderr,
        )
        return 1

    print(json.dumps({"status": "ok", "out_root": str(out_root)}, ensure_ascii=True))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
