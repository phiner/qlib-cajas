from pathlib import Path

from cajas.reports.validation_eurusd_dataset_audit import build_validation_eurusd_dataset_audit


def _write_csv(path: Path, text: str) -> Path:
    path.write_text(text, encoding="utf-8")
    return path


def test_audit_ready_single_file(tmp_path: Path) -> None:
    rows = ["Time (UTC),Open,High,Low,Close,Volume"]
    price = 1.1000
    for i in range(140):
        minute = (i * 15) % 60
        hour = (i * 15) // 60
        day = 1 + (hour // 24)
        hh = hour % 24
        o = price
        h = o + 0.0005
        l = o - 0.0004
        c = o + 0.0001
        rows.append(f"2025.01.{day:02d} {hh:02d}:{minute:02d}:00,{o:.5f},{h:.5f},{l:.5f},{c:.5f},100")
        price += 0.0001
    p = _write_csv(tmp_path / "eurusd.csv", "\n".join(rows))

    payload = build_validation_eurusd_dataset_audit(input_paths=[p])
    assert payload["status"] in {"ready", "watch"}
    assert payload["combined_coverage"]["row_count"] == 140
    assert payload["files"][0]["invalid_ohlc_relation_count"] == 0


def test_audit_blocked_missing_file(tmp_path: Path) -> None:
    payload = build_validation_eurusd_dataset_audit(input_paths=[tmp_path / "missing.csv"])
    assert payload["status"] == "blocked"


def test_audit_blocked_invalid_ohlc(tmp_path: Path) -> None:
    p = _write_csv(
        tmp_path / "bad.csv",
        "\n".join(
            [
                "Time (UTC),Open,High,Low,Close",
                "2025.01.01 00:00:00,1.1000,1.0900,1.0950,1.0990",
            ]
        ),
    )
    payload = build_validation_eurusd_dataset_audit(input_paths=[p])
    assert payload["status"] == "blocked"


